class Solution {
public:
    //8ms 91.98%
    //9.8MB 60.53%
    int longestValidParentheses(string s) {
        
        vector<int> dp(s.length(), 0);
        int ans = 0;
        for (int i = 1; i < s.length(); i++) {
            // cout << i << endl;
            // printVector(dp);
            if (s[i] == ')' && s[i-1] == '(') {
                if (i > 2) {
                    dp[i] = dp[i-2] + 2;
                } 
                else {
                    dp[i] = 2;
                }
            }
            else if (i-1-dp[i-1] >= 0 && s[i-1-dp[i-1]] == '(' && s[i] == ')') {
                if (i-2-dp[i-1] >= 0)
                    dp[i] = dp[i-1] + dp[i-2-dp[i-1]] + 2;
                else
                    dp[i] = dp[i-1] + 2;
            }
            ans = max(ans, dp[i]);
        } 

        // printVector(dp);

        return ans;
    }
};

